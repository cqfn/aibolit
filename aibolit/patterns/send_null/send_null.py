import javalang
from aibolit.utils.ast import AST


class SendNull:

    def __init__(self, filename: str):
        self.filename = filename

    def value(self):
        return [
            node.position.line
            for _, node in AST(self.filename).value().filter(javalang.tree.Invocation)
            if any(
                arg.value == 'null' for arg in node.arguments
            )
        ]
